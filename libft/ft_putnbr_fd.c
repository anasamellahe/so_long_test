#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
		ft_putchar_fd('-', fd);
	if ((long int)n * ((n > 0) - (n < 0)) > 9)
		ft_putnbr_fd(n / 10 * ((n > 0) - (n < 0)), fd);
	ft_putchar_fd((n % 10 * ((n > 0) - (n < 0))) + 48, fd);
}
